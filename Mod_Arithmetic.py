def do_add(num_1, num_2): # add two numbers
    return num_1 + num_2 

def do_sub(num_1, num_2): # subtracts two numbers
    return num_1 - num_2 

def do_multiply(num_1, num_2): # multiplies two numbers
    return num_1 * num_2 

def do_divide(num_1, num_2): # divides two numbers
    return num_1 / num_2

def do_divide_int(num_1, num_2): # quotient is an integer
    return num_1 // num_2

def do_modulus(num_1, num_2): # produces the remainder of a division
    return num_1 % num_2

def do_power(num_1, num_2): # exponential notation ; where num_1 - base, num_2 - exponent
    return num_1 ** num_2